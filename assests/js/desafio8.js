// Inicializa o contador com 10, pois queremos começar a partir de 10.
let contador = 10;

// Inicia um loop while que continuará enquanto o contador for maior ou igual a 0.
while (contador >= 0) {
    // Exibe o valor atual do contador no console.
    console.log(contador);

    // Decrementa o contador para passar para o próximo número.
    contador--;
}
