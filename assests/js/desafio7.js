// Inicializa o contador com 1, pois queremos começar a contar a partir de 1.
let contador = 1;

// Inicia um loop while que continuará enquanto o contador for menor ou igual a 10.
while (contador <= 10) {
    // Exibe o valor atual do contador no console.
    console.log(contador);

    // Incrementa o contador para passar para o próximo número.
    contador++;
}
